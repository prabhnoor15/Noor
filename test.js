'use strict';

const e = React.createElement;

class LikeButton extends React.Component {
  constructor(props) {
    super(props);
    this.state = { liked: false };
  }

  render() {
    if (this.state.liked) {
      return 'You liked this.';
    }

    return e(
      'button',
      { onClick: () => this.setState({ liked: true }) },
      'Like'
    );
  }
}

const domContainer = document.querySelector('#like_button_container');
//querySelector and getElementid has similar functioning getelemnt requires id but queryselector can selecct anything that a css selector can select wether id,,class,attribute ettc.
ReactDOM.render(e(LikeButton), domContainer);
